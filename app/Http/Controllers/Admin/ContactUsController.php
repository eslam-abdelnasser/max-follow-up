     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
        $message = ContactEmails::find($id);

        if($message->seen == 0){
            $message->seen ="1";
            $message->seen_by = Auth::id();
        }
        $message->save();

        $name = Admin::where('id',$message->seen_by)->value('name');
        return view('admin.contact-us.show')->with('name',$name)->with('message',$message);

    }

